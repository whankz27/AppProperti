    private void btn_prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_prosesActionPerformed
        // TODO add your handling code here:
        Checker();
        txtNo_Pelanggan.setText(""+no_pelanggan);
        cb_konfirmasi.setEnabled(true);
        Ltersedia = Integer.parseInt(txtLuasTanah_Asli.getText());
        Htanah = Integer.parseInt(txtHargaTanah.getText());
        Hbangunan = Integer.parseInt(txtHargaBangunan.getText());
        int tanah = Htanah * Ltersedia;
        int total = tanah + Hbangunan;
        txtHarga.setText("" + total);
        int nPpn = total/100*10;
        txtPPN.setText(""+nPpn);
        nDP = Integer.parseInt(txtDP.getText());
        int bayar = total + nPpn-nDP;
        nPeriode = Integer.parseInt(txtPeriode.getText());
        int nBulan = nPeriode;
        int nCicilan = bayar/nBulan;
        txtCicilan.setText(""+ nCicilan);
    }//GEN-LAST:event_btn_prosesActionPerformed

