    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        try{
           Connection conn = koneksi.getConnection();
           PreparedStatement stmt = conn.prepareStatement("INSERT INTO "
           + "penjualan(nama, area, harga_tanah, harga_bangunan, "
           + "tipe_rumah, luas_tanah_a, luas_tanah_t, harga, dp, periode, ppn, cicilan, no_pelanggan)"
           + "values(?,?,?,?,?,?,?,?,?,?,?,?,?)");
           
           stmt.setString(1, txtNama_Pemesan.getText());
           stmt.setString(2, area);
           stmt.setString(3, txtHargaTanah.getText());
           stmt.setString(4, txtHargaBangunan.getText());
           stmt.setString(5, tipe);
           stmt.setString(6, txtLuasTanah_Asli.getText());
           stmt.setString(7, txtLuasTanah_Tersedia.getText());
           stmt.setString(8, txtHarga.getText());
           stmt.setString(9, txtDP.getText());
           stmt.setString(10, txtPeriode.getText());
           stmt.setString(11, txtPPN.getText());
           stmt.setString(12, txtCicilan.getText());
           stmt.setString(13, no_pelanggan);

           stmt.executeUpdate();
           no_pelanggan = "";
           txtNo_Pelanggan.setText("");
           txtNama_Pemesan.setText("");
           cmb_area.setSelectedIndex(0);
           txtHargaTanah.setText("");
           txtHargaBangunan.setText("");
           rb36.setSelected(false);
           rb45.setSelected(false);
           rb54.setSelected(false);
           txtLuasTanah_Asli.setText("");
           txtLuasTanah_Tersedia.setText("");
           txtHarga.setText("");
           txtDP.setText("");
           txtPeriode.setText("");
           txtPPN.setText("");
           txtCicilan.setText("");
           cb_konfirmasi.setSelected(false);
           cb_konfirmasi.setEnabled(false);
           JOptionPane.showMessageDialog(this, "Berhasil ! ");
       } catch(SQLException e){
           JOptionPane.showMessageDialog(this, e);
           System.out.print(e);
       }
    }//GEN-LAST:event_btn_simpanActionPerformed

